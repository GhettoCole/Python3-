# Booleans - Represents two values True or False

my_boolean = True
print(my_boolean)

print(2 == 3)
print("Hello" == "Hello")

# Comparison - Another comparison operator is the 'not equal to' operator

print(1 != 1) # States that 1 is not equal to 1
print("Eleven" != "Seven")
print(2 != 10)

# Operator determining whether a value is bigger or smaller the other

print(7 > 10)
print(10 < 10)
print(10 <= 10) # Smaller than or equal to operators
print(10 >= 10) # Bigger than or equal to operators

